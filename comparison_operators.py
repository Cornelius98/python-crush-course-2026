#Equality by value only (==)
a = 20
b = 30

if a == b:
    print("Yes, is equal")
else:
    print("No, is not equal")


#Equality by value and data type (===)
c = 10
d = "10"

if c==d:
    print("Yes, is equal")
else:
    print("No, is not equal")

