#Data types file

#Integer data type
num_1 = 10
print(num_1)

#Float and double data types
float_num = 3.142
print(float_num)

#Character
char_1 = 'A'
print(char_1)

#String 
name = 'Guido Van Rossum'
print(name)

#List (or Array)
fruits = ["apple", "guava", "mango"]
print(fruits)

height = [1.5, 1.2, 1, 0.98, 2.5]
print(height)

#dictionary (Hash maps) key-value
founder = {
    "name": "Guido Van Rossum",
    "age": 10,
    "country": "Netherlands"
}

print(founder)