#Foreach Loop
fruits = [
    "Apple",
    "Mango",
    "Banana",
    "Orange",
    "Strawberry",
    "Pineapple",
    "Berries"
]

#Foreach loop iterating through all items in a list
for fruit_name in fruits:
    print(fruit_name)


#Print single array
print(fruits[0])
print(fruits[1])
print(fruits[2])
